// Robert Calero - App displaying switch statements

let age = 10;    // Variable initialization

// This switch statement will check to see if this user is in school based on their age
switch (age)
    {
        case 0:
            console.log("You have not been born yet");
            break;
        case 5:
            console.log("Too young for school");
            break;
        case 10:
            console.log("You are in school");
            break;
        default:
            console.log("Finished school");
            break;
    }